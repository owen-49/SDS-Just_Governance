export function findTopicById(topicId) {
  for (const s of sections) {
    for (const m of s.modules) {
      for (const t of m.topics) {
        if (t.id === topicId) return { section: s, module: m, topic: t };
      }
    }
  }
  return null;
}

export function findModuleById(moduleId) {
  for (const s of sections) {
    for (const m of s.modules) {
      if (m.id === moduleId) return { section: s, module: m };
    }
  }
  return null;
}

export function getAllModules() {
  const modules = [];
  for (const s of sections) {
    for (const m of s.modules) {
      modules.push({ ...m, sectionName: s.name, sectionId: s.id });
    }
  }
  return modules;
}
